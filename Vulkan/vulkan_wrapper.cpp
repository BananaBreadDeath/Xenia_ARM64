VulkanWrapper::ErrorCode VulkanWrapper::createTextureImage(const std::string& path, XboxTexture& texture) {
    AAsset* asset = AAssetManager_open(assetManager, path.c_str(), AASSET_MODE_BUFFER);
    if (!asset) {
        return ErrorCode::FILE_NOT_FOUND;
    }
    
    stbi_uc* pixels = stbi_load_from_memory(
        static_cast<const stbi_uc*>(AAsset_getBuffer(asset)),
        AAsset_getLength(asset),
        &texture.width,
        &texture.height,
        nullptr,
        STBI_rgb_alpha
    );
    AAsset_close(asset);
    
    if (!pixels) {
        return ErrorCode::FILE_NOT_FOUND;
    }
    
    VkDeviceSize imageSize = texture.width * texture.height * 4;
    
    VkBuffer stagingBuffer;
    VkDeviceMemory stagingBufferMemory;
    VK_CHECK(createBuffer(imageSize, VK_BUFFER_USAGE_TRANSFER_SRC_BIT,
                         VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT,
                         stagingBuffer, stagingBufferMemory));
    
    void* data;
    vkMapMemory(device, stagingBufferMemory, 0, imageSize, 0, &data);
    memcpy(data, pixels, static_cast<size_t>(imageSize));
    vkUnmapMemory(device, stagingBufferMemory);
    
    stbi_image_free(pixels);
    
    VkImageCreateInfo imageInfo{};
    imageInfo.sType = VK_STRUCTURE_TYPE_IMAGE_CREATE_INFO;
    imageInfo.imageType = VK_IMAGE_TYPE_2D;
    imageInfo.extent.width = static_cast<uint32_t>(texture.width);
    imageInfo.extent.height = static_cast<uint32_t>(texture.height);
    imageInfo.extent.depth = 1;
    imageInfo.mipLevels = 1;
    imageInfo.arrayLayers = 1;
    imageInfo.format = VK_FORMAT_R8G8B8A8_SRGB;
    imageInfo.tiling = VK_IMAGE_TILING_OPTIMAL;
    imageInfo.initialLayout = VK_IMAGE_LAYOUT_UNDEFINED;
    imageInfo.usage = VK_IMAGE_USAGE_TRANSFER_DST_BIT | VK_IMAGE_USAGE_SAMPLED_BIT;
    imageInfo.sharingMode = VK_SHARING_MODE_EXCLUSIVE;
    imageInfo.samples = VK_SAMPLE_COUNT_1_BIT;
    
    if (vkCreateImage(device, &imageInfo, nullptr, &texture.image) != VK_SUCCESS) {
        vkDestroyBuffer(device, stagingBuffer, nullptr);
        vkFreeMemory(device, stagingBufferMemory, nullptr);
        return ErrorCode::OUT_OF_MEMORY;
    }
    
    VkMemoryRequirements memRequirements;
    vkGetImageMemoryRequirements(device, texture.image, &memRequirements);
    
    VkMemoryAllocateInfo allocInfo{};
    allocInfo.sType = VK_STRUCTURE_TYPE_MEMORY_ALLOCATE_INFO;
    allocInfo.allocationSize = memRequirements.size;
    allocInfo.memoryTypeIndex = findMemoryType(memRequirements.memoryTypeBits, 
                                             VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT);
    
    if (vkAllocateMemory(device, &allocInfo, nullptr, &texture.memory) != VK_SUCCESS) {
        vkDestroyImage(device, texture.image, nullptr);
        vkDestroyBuffer(device, stagingBuffer, nullptr);
        vkFreeMemory(device, stagingBufferMemory, nullptr);
        return ErrorCode::OUT_OF_MEMORY;
    }
    
    vkBindImageMemory(device, texture.image, texture.memory, 0);
    
    VK_CHECK(transitionImageLayout(texture.image, VK_FORMAT_R8G8B8A8_SRGB, 
                                 VK_IMAGE_LAYOUT_UNDEFINED, VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL));
    VK_CHECK(copyBufferToImage(stagingBuffer, texture.image, 
                             static_cast<uint32_t>(texture.width), 
                             static_cast<uint32_t>(texture.height)));
    VK_CHECK(transitionImageLayout(texture.image, VK_FORMAT_R8G8B8A8_SRGB, 
                                 VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL, 
                                 VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL));
    
    vkDestroyBuffer(device, stagingBuffer, nullptr);
    vkFreeMemory(device, stagingBufferMemory, nullptr);
    
    return ErrorCode::SUCCESS;
}

